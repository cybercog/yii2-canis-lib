<?php
namespace infinite\data;

class Pagination extends \yii\data\Pagination {
	protected $_state;

	/**
	 *
	 *
	 * @return unknown
	 */
	public function getState() {
		return $this->_state;
	}


	/**
	 *
	 *
	 * @param unknown $state
	 */
	public function setState($state) {
		$this->_state = $state;
	}
}